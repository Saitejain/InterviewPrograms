package intpracticeprgms;

public class Print1to100 {

	public static void main(String[] args) {
		int one='A'/'A';
		String s="..........";
		for(int i=one;i<=(s.length()*s.length());i++)
				{
			      System.out.println(i);
				}
	//AscI values for a=97 b=98 c=99 d=100
		for(int j=one;j<='d';j++)
			
		{
			System.out.println(j);
		}

	}

}
